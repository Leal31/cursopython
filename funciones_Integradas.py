#Funciones integradas

n = int(10)
print(n)
print(type(n))

n = str(10)
print(n)
print(type(n))

n = bin(10)
print(n)
print(type(n))

n = hex(10)
print(n)
print(type(n))

n = int("0b1010",2)
print(n)
print(type(n))

n = int("0xa", 16)
print(n)
print(type(n))

n = abs(-8)
print(n)
print(type(n))

n = round(7.8)
print(n)
print(type(n))

n = len("Emanuel")
print(n)
print(type(n))
